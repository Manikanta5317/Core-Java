class F 
{
	public static void main(String[] args) 
	{
		System.out.println(Integer.toBinaryString(192));
		System.out.println(Integer.toBinaryString(617));
		System.out.println(Integer.toBinaryString(255));
		System.out.println(192 | 617);//bitwise or
		System.out.println(192 & 617);//bitwise and
		System.out.println(192 ^ 617);//bitwise Xor
	}
}
